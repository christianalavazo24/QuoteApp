package com.example.quoteapp;

// Quote class represents a quote with its author and category
public class Quote {
    private String author;  // The author of the quote
    private String quote;   // The text of the quote
    private String category; // The category of the quote

    // Getter method for the author
    public String getAuthor() {
        return author;
    }

    // Getter method for the quote
    public String getQuote() {
        return quote;
    }

    // Getter method for the category
    public String getCategory() {
        return category;
    }
}
